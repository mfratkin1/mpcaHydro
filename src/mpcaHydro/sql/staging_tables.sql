SELECT 
    CAST(NULL AS DOUBLE) AS LATITUDE,
    CAST(NULL AS DOUBLE) AS LONGITUDE,
    CAST(NULL AS VARCHAR) AS WID_LIST,
    CAST(NULL AS VARCHAR) AS SAMPLE_METHOD,
    CAST(NULL AS VARCHAR) AS SAMPLE_REMARK,
    CAST(NULL AS BIGINT) AS FACILITY_ID,
    CAST(NULL AS VARCHAR) AS FACILITY_NAME,
    CAST(NULL AS VARCHAR) AS FACILITY_TYPE,
    CAST(NULL AS VARCHAR) AS SYS_LOC_CODE,
    CAST(NULL AS VARCHAR) AS LOC_NAME,
    CAST(NULL AS VARCHAR) AS LOC_TYPE,
    CAST(NULL AS VARCHAR) AS LOC_TYPE_2,
    CAST(NULL AS VARCHAR) AS TASK_CODE,
    CAST(NULL AS BIGINT) AS SAMPLE_ID,
    CAST(NULL AS VARCHAR) AS SYS_SAMPLE_CODE,
    CAST(NULL AS BIGINT) AS TEST_ID,
    CAST(NULL AS VARCHAR) AS ANALYTE_TYPE,
    CAST(NULL AS VARCHAR) AS ANALYTE_TYPE_DESC,
    CAST(NULL AS VARCHAR) AS ANALYTIC_METHOD,
    CAST(NULL AS VARCHAR) AS PREFERRED_NAME,
    CAST(NULL AS VARCHAR) AS PARAMETER,
    CAST(NULL AS VARCHAR) AS CAS_RN,
    CAST(NULL AS VARCHAR) AS CHEMICAL_NAME,
    CAST(NULL AS VARCHAR) AS GTLT,
    CAST(NULL AS VARCHAR) AS RESULT_TEXT,
    CAST(NULL AS DOUBLE) AS RESULT_NUMERIC,
    CAST(NULL AS VARCHAR) AS RESULT_UNIT,
    CAST(NULL AS INTEGER) AS STAT_TYPE,
    CAST(NULL AS VARCHAR) AS VALUE_TYPE,
    CAST(NULL AS VARCHAR) AS DETECT_FLAG,
    CAST(NULL AS VARCHAR) AS DETECT_DESC,
    CAST(NULL AS VARCHAR) AS RESULT_REMARK,
    CAST(NULL AS VARCHAR) AS RESULT_TYPE_CODE,
    CAST(NULL AS VARCHAR) AS METHOD_DETECTION_LIMIT,
    CAST(NULL AS VARCHAR) AS REPORTING_DETECTION_LIMIT,
    CAST(NULL AS INTEGER) AS QUANTITATION_LIMIT,
    CAST(NULL AS VARCHAR) AS LAB_QUALIFIERS,
    CAST(NULL AS VARCHAR) AS INTERPRETED_QUALIFIERS,
    CAST(NULL AS VARCHAR) AS REPORTABLE_RESULT,
    CAST(NULL AS VARCHAR) AS APPROVAL_CODE,
    CAST(NULL AS VARCHAR) AS SENSITIVE_NOTPUBLIC,
    CAST(NULL AS VARCHAR) AS TEST_TYPE,
    CAST(NULL AS DOUBLE) AS DILUTION_FACTOR,
    CAST(NULL AS VARCHAR) AS FRACTION,
    CAST(NULL AS VARCHAR) AS BASIS,
    CAST(NULL AS VARCHAR) AS TEMP_BASIS,
    CAST(NULL AS VARCHAR) AS TEST_REMARK,
    CAST(NULL AS TIMESTAMP_NS) AS ANALYSIS_DATE_TIME,
    CAST(NULL AS VARCHAR) AS ANALYSIS_DATE,
    CAST(NULL AS VARCHAR) AS ANALYSIS_TIME,
    CAST(NULL AS VARCHAR) AS ANALYSIS_DATE_TIMEZONE,
    CAST(NULL AS VARCHAR) AS COMPANY_NAME,
    CAST(NULL AS VARCHAR) AS LAB_NAME_CODE,
    CAST(NULL AS VARCHAR) AS LAB_SAMPLE_ID,
    CAST(NULL AS VARCHAR) AS SAMPLE_TYPE_GROUP,
    CAST(NULL AS VARCHAR) AS SAMPLE_TYPE_CODE,
    CAST(NULL AS VARCHAR) AS SAMPLE_TYPE_DESC,
    CAST(NULL AS VARCHAR) AS MEDIUM_CODE,
    CAST(NULL AS VARCHAR) AS MATRIX_CODE,
    CAST(NULL AS DOUBLE) AS START_DEPTH,
    CAST(NULL AS VARCHAR) AS DEPTH_UNIT,
    CAST(NULL AS TIMESTAMP_NS) AS SAMPLE_DATE_TIME,
    CAST(NULL AS VARCHAR) AS SAMPLE_DATE,
    CAST(NULL AS VARCHAR) AS SAMPLE_TIME,
    CAST(NULL AS VARCHAR) AS SAMPLE_DATE_TIMEZONE,
    CAST(NULL AS DOUBLE) AS EBATCH
WHERE FALSE;

-- staging.wiski schema
CREATE OR REPLACE VIEW staging.wiski AS 
SELECT 
    CAST(NULL AS DATETIME) AS "Timestamp",
    CAST(NULL AS DOUBLE) AS "Value",
    CAST(NULL AS BIGINT) AS "Quality Code",
    CAST(NULL AS VARCHAR) AS "Quality Code Name",
    CAST(NULL AS VARCHAR) AS ts_unitsymbol,
    CAST(NULL AS VARCHAR) AS ts_name,
    CAST(NULL AS VARCHAR) AS ts_id,
    CAST(NULL AS VARCHAR) AS station_no,
    CAST(NULL AS VARCHAR) AS station_name,
    CAST(NULL AS VARCHAR) AS station_latitude,
    CAST(NULL AS VARCHAR) AS station_longitude,
    CAST(NULL AS VARCHAR) AS parametertype_id,
    CAST(NULL AS VARCHAR) AS parametertype_name,
    CAST(NULL AS VARCHAR) AS stationparameter_no,
    CAST(NULL AS VARCHAR) AS stationparameter_name,
    CAST(NULL AS BIGINT) AS wplmn_flag
WHERE FALSE;
